-- 业务需求：计算每个SKU本周（5.16-5.22）相对上周（5.10-5.15）的GMV环比增长率
-- 核心逻辑：用LAG窗口函数按SKU分组、按周期排序，取出上一周的GMV，再用环比公式计算
SELECT
    period,                  -- 统计周期（当前周）
    sku_name,                -- 商品SKU名称
    gmv,                     -- 本周GMV（当前周期成交金额）
    -- 窗口函数LAG：取同一SKU的上一周期GMV（上周GMV）
    -- PARTITION BY sku_name：按商品分组，确保只在同一个SKU内取历史数据
    -- ORDER BY period：按时间周期升序排序，确保取到的是上一周的最新数据
    LAG(gmv, 1) OVER (
        PARTITION BY sku_name 
        ORDER BY period
    ) AS last_week_gmv,      -- 别名：上周GMV
    -- 标准环比增长率公式：(本周GMV - 上周GMV) / 上周GMV × 100，保留2位小数
    -- 业务意义：正数代表增长，负数代表下滑，数值越大增长势头越猛
    ROUND(
        (gmv - LAG(gmv, 1) OVER (PARTITION BY sku_name ORDER BY period)) 
        / LAG(gmv, 1) OVER (PARTITION BY sku_name ORDER BY period) 
        * 100, 
    2) AS gmv_week_on_week_growth_pct  -- 别名：GMV周环比增长率（%）
FROM product_sales1
-- 按商品名称+周期排序，同一个SKU的两周数据会排在一起，方便对比
ORDER BY sku_name, period;

select 
      period,
      sku_name,
      gmv,
      lag(gmv,1)over(partition by sku_name order by period) as last_gmv,
       round((gmv - LAG(gmv,1) OVER (PARTITION BY sku_name ORDER BY period))
    / LAG(gmv,1) OVER (PARTITION BY sku_name ORDER BY period)
    * 100,2) as gmv_growth_pct
from product_sales1

